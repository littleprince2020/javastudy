package com.llittleprinc.operator;

public class Damo03 {
    public static void main(String[] args) {
        //关系运算符返回的结果为布尔值：true / false
        int a = 10;
        int b = 20;

        System.out.println(a==b);//false
        System.out.println(a>b);//false
        System.out.println(a<b);//true
        System.out.println(a !=b) ;//true;

    }
}
